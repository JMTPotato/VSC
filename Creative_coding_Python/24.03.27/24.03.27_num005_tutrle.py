import turtle
t = turtle.Turtle()
t.shape('turtle')

t.fillcolor('blue')
t.begin_fill()
t.circle(50)
t.end_fill()

t.up()
t.goto(90, 0)
t.down()

t.fillcolor('black')
t.begin_fill()
t.circle(50)
t.end_fill()

t.up()
t.goto(180, 0)
t.down()

t.fillcolor('red')
t.begin_fill()
t.circle(50)
t.end_fill()

t.up()
t.goto(45, -50)
t.down()

t.fillcolor('yellow')
t.begin_fill()
t.circle(50)
t.end_fill()

t.up()
t.goto(135, -50)
t.down()

t.fillcolor('green')
t.begin_fill()
t.circle(50)
t.end_fill()

